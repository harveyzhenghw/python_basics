# > grater then
# < less then
# == equal
# != not equal
# <= less then or equal
# >= grater then or equal

print(12 > 15) # False # ==, <, !=, <=
print(5 < 15000) # True # ==, >, >=, <=
print(120 != 120) # False # ==, >=, !=, <=
print(60 < 15) # False # ==, <, !=, >=
print(25.3421 == 25.3421) # True # ==, <, !=, <=
