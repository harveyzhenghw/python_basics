day_in_a_week = int(input("enter day of the week as a number: "))



if day_in_a_week == 1:
    print("monday")
elif day_in_a_week == 2:
    print("tuesday")
elif day_in_a_week == 3:
    print("wedsday")
elif day_in_a_week == 4:
    print("thursday")
elif day_in_a_week == 5:
    print("friday")
elif day_in_a_week == 6:
    print("saturday")
elif day_in_a_week == 7:
    print("sunday")
else:
    print(f"{day_in_a_week} doesn't exist")